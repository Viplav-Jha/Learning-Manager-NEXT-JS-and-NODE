// add review in course
interface IAddReviewData {
  review: string;
  courseId: string;
  rating: number;
  user: string;
}

export const addReview = catchAsyncError(
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const userCourseList = req.user.courses;

      const courseId = req.params.id;

      //check if courseid already exists in userCourseList base on _id
      const courseExists = userCourseList?.some(
        (course: any) => course._id.toString() === courseId.toString()
      );

      if (!courseExists) {
        return next(
          new ErrorHandler("You are not eliglble to access this course", 404)
        );
      }

      const course = await CourseModel.findById(courseId);
      const { review, rating } = req.body as IAddReviewData;

      const reviewData: any = {
        user: req.user,
        comment: review,
        rating,
      };

      course?.reviews.push(reviewData);

      let avg = 0;

      course?.reviews.forEach((rev: any) => {
        avg += rev.rating;
      });

      if (course) {
        course.rating = avg / course.reviews.length; //one example we have 2 reviews one is 5 another is 4 so math working like this = 9 /2 = 4.5 rating
      }

      await course?.save(); //saving review in mogodb

      const notification = {
        title: "New Review Reveived",
        message: `${req.user?.name} has given on your review in${course?.name}`,
      };
      //create notification
      res.status(200).json({
        success: true,
        course,
      });
    } catch (error: any) {
      return next(new ErrorHandler(error.message, 500));
    }
  }
);

